public class Trabajador extends Persona {
    
    private long telefono;
    private double costo_del_envio;
    
    public long getTelefono(){
        return telefono;
    }
    public void setTelefono(long t){
        telefono=t;
    }
    public double getCosto_de_envio(){
        return costo_del_envio;
    }
    public void setCosto_de_envio(double c){
        costo_del_envio=c;
    }
    
}
    


